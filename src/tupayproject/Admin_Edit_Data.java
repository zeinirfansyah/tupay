	// Mengembalikan input data pengguna ke awal.
	public void reset() {
		txt_StudentNumber.setText(studentId);
		txt_credits.setText(String.valueOf(studentCredits));
		txt_name.setText(studentName);
		cmb_departement.setSelectedItem(studentDepartment);
		cmb_faculty.setSelectedItem(studentFaculty);
		cmb_generation.setSelectedItem(studentGeneration);
		cmb_semester.setSelectedItem(studentSemester);
		ta_print.setText(null);
	}

	private void setEditableData(JComboBox jComboBox, String data) {
		for (int index = 0; index < jComboBox.getModel().getSize(); index++) {
			if (jComboBox.getModel().getElementAt(index).toString().equals(data) && jComboBox.getModel().getElementAt(index).toString() != null) {
				jComboBox.setSelectedIndex(index);
				break;
			}
		}
	}

