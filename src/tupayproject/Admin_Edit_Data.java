	private void btn_submit_editedMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_submit_editedMouseClicked
		// Kueri untuk menyimpan data hasil edit
		try {
			PreparedStatement saveStatement = connection.prepareStatement("update transaction_list " +
					"set student_name = ?, " +
					"student_id = ?, " +
					"student_faculty = ?, " +
					"student_major = ?, " +
					"student_batch = ?, " +
					"student_semester = ?, " +
					"student_credit = ?," +
					"cf_fixed = ?, " +
					"cf_variable = ?, " +
					"cf_total = ?" +
					"where transaction_id = ?"
			);
			saveStatement.setString(1, txt_name.getText());
			saveStatement.setString(2, txt_StudentNumber.getText());
			saveStatement.setString(3, cmb_faculty.getSelectedItem().toString());
			saveStatement.setString(4, cmb_departement.getSelectedItem().toString());
			saveStatement.setString(5, cmb_generation.getSelectedItem().toString());
			saveStatement.setString(6, cmb_semester.getSelectedItem().toString());

			saveStatement.setInt(7, Integer.parseInt(txt_credits.getText()));

			saveStatement.setDouble(8, TuitionBill.TB_Fixed(cmb_generation.getSelectedIndex(), cmb_faculty.getSelectedIndex()));
			saveStatement.setDouble(9, TuitionBill.TB_Variable(cmb_generation.getSelectedIndex(), cmb_departement.getSelectedIndex(), Double.parseDouble(txt_credits.getText())));
			saveStatement.setDouble(10, TuitionBill.TB_Total(cmb_generation.getSelectedIndex(), cmb_faculty.getSelectedIndex(), cmb_departement.getSelectedIndex(), Double.parseDouble(txt_credits.getText())));

			saveStatement.setInt(11, this.transactionId);

			saveStatement.execute();
			JOptionPane.showMessageDialog(this, "Edited data has been successfully saved.", "Success", JOptionPane.INFORMATION_MESSAGE);
			connection.close();

		} catch (SQLException e) {
			JOptionPane.showMessageDialog(this, "Error occured during saving data. Please try again.", "Save Error", JOptionPane.ERROR_MESSAGE);
			e.printStackTrace();
		}
		dispose();
		new Admin_TransactionList().setVisible(true);
	}//GEN-LAST:event_btn_submit_editedMouseClicked

