	public Admin_Edit_Data(int transactionId) {
		initComponents();
		setLocationRelativeTo(null);
		setResizable(false);

		this.transactionId = transactionId;

		// Membuat text area yang tidak bisa diedit
		ta_print.setEditable(false);

		try {
			connection = DBConnection.connectDB("8001"); // Anda dapat menggantinya menjadi 3306 (Port default) jika Anda tidak ingin menggunakan Docker sebagai tempat database.

			PreparedStatement formStatement = connection.prepareStatement("select * from transaction_list where transaction_id = ?");
			formStatement.setInt(1, transactionId);

			ResultSet formResult = formStatement.executeQuery(); // Menjalankan kueri SQL.

			// Mengambil data pada hasil kueri yang telah dijalankan.
			if (formResult.next()) {
				// Menyimpan hasil data kueri kedalam variabel.
				studentName = formResult.getString(2);
				studentId = formResult.getString(3);
				studentFaculty = formResult.getString(4);
				studentDepartment = formResult.getString(5);
				studentGeneration = formResult.getString(6);
				studentSemester = formResult.getString(7);
				studentCredits = formResult.getInt(8);

				// JTextField
				txt_name.setText(studentName);
				txt_StudentNumber.setText(studentId);
				txt_credits.setText(String.valueOf(studentCredits));

				// JComboBox
				setEditableData(cmb_faculty, studentFaculty);
				setEditableData(cmb_departement, studentDepartment);
				setEditableData(cmb_generation, studentGeneration);
				setEditableData(cmb_semester, studentSemester);
			}
		} catch (SQLException e) {
			e.printStackTrace();
		}
	}

